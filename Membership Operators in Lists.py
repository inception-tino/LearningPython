# Title: Membership Operators in Lists

# Initialize a list with some values
a = [11, 22, 33, 44, 55, 66, 77, 88]

# Check if 11 is in the list a
print(11 in a)  # True, because 11 is in the list

# Check if 222 is in the list a
print(222 in a)  # False, because 222 is not in the list

# Check if 11 is not in the list a
print(11 not in a)  # False, because 11 is in the list

# Check if 111 is not in the list a
print(111 not in a)  # True, because 111 is not in the list
