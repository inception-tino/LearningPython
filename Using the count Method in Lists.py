# Title: Using the `count` Method in Lists

# Initialize a list with some values
l = [11, 22, 33, 44, 55, 66, 22, 77, 22, 99, 100, 111]

# Find the number of occurrences of 22 in the list
print(l.count(22))  # Output: 3

# Find the number of occurrences of 222 in the list
print(l.count(222))  # Output: 0
